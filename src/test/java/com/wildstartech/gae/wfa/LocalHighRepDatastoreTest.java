package com.wildstartech.gae.wfa;

import org.testng.annotations.AfterMethod;
import org.testng.annotations.Test;
import org.testng.annotations.BeforeMethod;
import org.testng.AssertJUnit;
import com.google.appengine.api.datastore.DatastoreService;
import com.google.appengine.api.datastore.DatastoreServiceFactory;
import com.google.appengine.api.datastore.Entity;
import static com.google.appengine.api.datastore.FetchOptions.Builder.withLimit;
import com.google.appengine.api.datastore.Key;
import com.google.appengine.api.datastore.KeyFactory;
import com.google.appengine.api.datastore.Query;
import com.google.appengine.tools.development.testing.LocalDatastoreServiceTestConfig;
import com.google.appengine.tools.development.testing.LocalServiceTestHelper;

public class LocalHighRepDatastoreTest {

    // maximum eventual consistency
    private final LocalServiceTestHelper helper =
        new LocalServiceTestHelper(new LocalDatastoreServiceTestConfig()
            .setDefaultHighRepJobPolicyUnappliedJobPercentage(100));

    @BeforeMethod
    public void setUp() {
        helper.setUp();
    }

    @AfterMethod
    public void tearDown() {
        helper.tearDown();
    }

    @Test
    public void testEventuallyConsistentGlobalQueryResult() {
        DatastoreService ds = DatastoreServiceFactory.getDatastoreService();
        Key ancestor = KeyFactory.createKey("foo", 3);
        ds.put(new Entity("yam", ancestor));
        ds.put(new Entity("yam", ancestor));
        // global query doesn't see the data
        AssertJUnit.assertEquals(0, ds.prepare(
              new Query("yam")).countEntities(withLimit(10)));
        // ancestor query does see the data
        AssertJUnit.assertEquals(2, ds.prepare(
              new Query("yam", ancestor)).countEntities(withLimit(10)));
    }
}